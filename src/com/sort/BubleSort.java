	// 冒泡排序 无优化
	public static int[] naiveBubleSort(int[] v) {
		int len = v.length;
		int i, j, swap;

		for (i = 0; i < len - 1; i++) {
			for (j = len - 1; j > i; j--) {
				if (v[j] < v[j - 1]) {
					swap = v[j];
					v[j] = v[j - 1];
					v[j - 1] = swap;
				}
			}
		}
		return v;
	}

	// 优化
	public static int[] optBubleSort(int[] v) {
		int len = v.length;
		int i, j, swap;

		boolean swap_ornot = false;
		for (i = 0; i < len - 1; i++) {
			for (j = len - 1; j > i; j--) {
				if (v[j] < v[j - 1]) {
					swap = v[j];
					v[j] = v[j - 1];
					v[j - 1] = swap;
					swap_ornot = true;
				}
			}
			if (!swap_ornot)
				return v;
		}
		return v;
	}

